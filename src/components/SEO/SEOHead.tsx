import { Helmet } from "react-helmet-async";

interface SEOHeadProps {
  title?: string;
  description?: string;
  keywords?: string;
  canonical?: string;
  ogImage?: string;
  structuredData?: object;
}

export function SEOHead({
  title = "KNSOFT Technologies - Expert IT Solutions & Software Development",
  description = "KNSOFT Technologies delivers cutting-edge IT consulting, custom software development, web applications, mobile apps, ecommerce solutions & ERP systems. Transform your business with our 10+ years of proven expertise.",
  keywords = "IT solutions, software development, digital transformation, web development, mobile app development, ecommerce, ERP systems, IT consulting, custom software, technology services",
  canonical,
  ogImage = "/knsoftnewone.png",
  structuredData
}: SEOHeadProps) {
  return (
    <Helmet>
      <title>{title}</title>
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      <meta name="robots" content="index, follow" />
      
      {canonical && <link rel="canonical" href={canonical} />}
      
      {/* Open Graph */}
      <meta property="og:title" content={title} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={ogImage} />
      <meta property="og:type" content="website" />
      
      {/* Twitter */}
      <meta name="twitter:title" content={title} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:image" content={ogImage} />
      <meta name="twitter:card" content="summary_large_image" />
      
      {/* Structured Data */}
      {structuredData && (
        <script type="application/ld+json">
          {JSON.stringify(structuredData)}
        </script>
      )}
    </Helmet>
  );
}